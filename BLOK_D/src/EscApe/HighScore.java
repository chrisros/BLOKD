    private void tableInhoud() {
        // TODO add your handling code here:
        Sql_connect.doConnect();
        // declareer de variable voor in de rs
        String naam;
        int stappen;
        int score;
        String datum;

        try {
            // connect 
            Sql_connect.doConnect();

            ResultSet result;
            // statement maken
            PreparedStatement stat = Sql_connect.getConnection().prepareStatement("SELECT * FROM blokD ORDER BY score DESC LIMIT 10");
            result = stat.executeQuery();
            // rijen
            int i = 0;

            while (result.next()) {
                i++;

            }
            table.setRowCount(i);
            result.beforeFirst();

            int d = 0;
            while (result.next()) {
                //Stop de variable in een rs
                naam = result.getString("naam");
                stappen = result.getInt("stappen");
                score = result.getInt("score");
                datum = result.getString("datum");

                // vul vervolgens in de tabel de waardes in als volgt: resultset, aantal, plaats
                table.setValueAt(naam, d, 0);
                table.setValueAt(stappen, d, 1);
                table.setValueAt(score, d, 2);
                table.setValueAt(datum, d, 3);
                // verhoog aantal totdat alles was je hebt opgevraagd is geweest
                d++;

            }

            result.last();
            result.close();
            //stat.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

